const aboutsContainers = document.getElementById("abouts-containers");

aboutsData.forEach((about, index) => {
    const aboutContainer = document.createElement("div");
    aboutContainer.classList.add("about-container");


    if (index % 2 !== 0) {
        aboutContainer.classList.add("reverse-content");
    }

    // Cria a imagem
    const img = document.createElement("img");
    img.src = about.imgSrc;
    img.alt = `${about.title}-IMG`;

    img.style.animation = `floating_2 ${Math.random() * (10 - 5) + 5}s ease-in-out infinite`;

    aboutContainer.appendChild(img);

    const textContent = document.createElement("div");
    textContent.id = "text-content";
    
    if (index % 2 !== 0) {
        textContent.classList.add("reverse-content");
    }

    const h1 = document.createElement("h1");
    h1.textContent = about.title;
    textContent.appendChild(h1);

    const h2 = document.createElement("h2");
    h2.textContent = about.subtitle;
    textContent.appendChild(h2);

    // Adiciona a descrição
    const p = document.createElement("p");
    p.innerHTML = about.description;
    textContent.appendChild(p);

    aboutContainer.appendChild(textContent);

    aboutsContainers.appendChild(aboutContainer);
});

document.addEventListener('DOMContentLoaded', () => {
    let timePlayed = 0;

    const sounds = {
        sas: {
            initial: new Audio('./audios/voice_sans.mp3'),
            transform: new Audio('./audios/sans_transform.mp3'),
            badTime: new Audio('./audios/bad_time.mp3')
        },
        mm2: {
            shoot: new Audio('./audios/mm2Shooting.mp3'),
            win: new Audio('./audios/mm2Win.mp3')
        },
        burguer: {
            nomNomNom: new Audio('./audios/nomnomnom.mp3'),
            chezburguer: new Audio('./audios/chezburguer.mp3')
        },
        pizza: {
            pizzah: new Audio('./audios/pizza.mp3')
        },
        netflix: {
            tudum: new Audio('./audios/netflix_intro.mp3')
        }
    };

    const playSound = (audio) => {
        audio.currentTime = 0;
        audio.play();
    };

    const triggerGasterEffect = (icon) => {
        const smallImg = document.createElement('img');
        smallImg.src = 'images/EASTEREGGS/sas/Gaster/Gaster_1.svg';
        smallImg.alt = 'Easter Egg';

        Object.assign(smallImg.style, {
            position: 'fixed',
            top: '50%',  // Centraliza verticalmente
            left: '50%', // Centraliza horizontalmente
            transform: 'translate(-50%, -50%)', // Ajuste fino para centralização perfeita
            width: '500px',
            height: 'auto',
            zIndex: '1000',
        });        

        document.body.appendChild(smallImg);
        setTimeout(() => (smallImg.style.right = '0'), 50);

        setTimeout(() => (smallImg.src = 'images/EASTEREGGS/sas/Gaster/Gaster_2.svg'), 2000);

        setTimeout(() => {
            const whiteEffect = document.createElement('div');
            Object.assign(whiteEffect.style, {
                position: 'fixed',
                top: '0',
                left: '0',
                width: '100vw',
                height: '100vh',
                backgroundColor: 'white',
                zIndex: '100000',
                opacity: '1',
                transition: 'opacity 1s ease'
            });

            document.body.appendChild(whiteEffect);
            setTimeout(() => {
                smallImg.remove();
                icon.src = 'images/EASTEREGGS/sas/sas.png';
                timePlayed = 0;
            }, 1000);

            setTimeout(() => {
                whiteEffect.style.opacity = '0';
                whiteEffect.addEventListener('transitionend', () => whiteEffect.remove());
            }, 3000);
        }, 3070);
    };

    document.addEventListener('click', (event) => {
        if (event.target.classList.contains('sound-icon')) {
            const icon = event.target;
            const info = icon.getAttribute('data-sound');

            if (info === 'sas') {
                if (timePlayed >= 5) {
                    playSound(sounds.sas.transform);
                    icon.src = 'images/EASTEREGGS/sas/sasTransform.png';
                    timePlayed++;
                    if (timePlayed >= 6) {
                        playSound(sounds.sas.badTime);
                        triggerGasterEffect(icon);
                        timePlayed = 0;
                    }
                } else {
                    playSound(sounds.sas.initial);
                    timePlayed++;
                }
            } else if (info === 'mm2') {
                if (timePlayed >= 4) {
                    playSound(sounds.mm2.win);
                    timePlayed = 0;
                } else {
                    playSound(sounds.mm2.shoot);
                    timePlayed++;
                }
            } else if (info === 'burguer') {
                if (timePlayed >= 6) {
                    playSound(sounds.burguer.chezburguer);
                    timePlayed = 0;
                } else {
                    playSound(sounds.burguer.nomNomNom);
                    timePlayed++;
                }
            } else if (info === 'pizza') {
                playSound(sounds.pizza.pizzah)
            } else if (info === 'netflix') {
                playSound(sounds.netflix.tudum)
            }

            icon.classList.add('jump');
            icon.addEventListener('animationend', () => icon.classList.remove('jump'), { once: true });
        }
    });
});
